// Generates src/lib/docs-sitemap.generated.json — the list of docs page URLs
// derived from the MDX tree.
//
// The sitemap route runs on the edge runtime, which has no node:fs/node:path.
// Fumadocs' source loader reaches for both, so importing it from the route
// breaks the edge build. Precomputing the URLs here (plain data) keeps the
// route's import graph edge-clean. Regenerated on prebuild/predev/typecheck.
import { readdir, writeFile } from "node:fs/promises"
import { dirname, join, relative, resolve } from "node:path"
import { fileURLToPath } from "node:url"

const here = dirname(fileURLToPath(import.meta.url))
const root = resolve(here, "..")
const contentDir = join(root, "src/app/(docs)/content/docs")
const outFile = join(root, "src/lib/docs-sitemap.generated.json")
const baseUrl = "/docs"

async function walk(dir) {
	const entries = await readdir(dir, { withFileTypes: true })
	const files = []
	for (const entry of entries) {
		const full = join(dir, entry.name)
		if (entry.isDirectory()) files.push(...(await walk(full)))
		else if (entry.isFile() && entry.name.endsWith(".mdx")) files.push(full)
	}
	return files
}

// Mirror fumadocs' slug rules: strip the extension, and treat `index` as the
// folder's own URL. No nested index files today, but the rule is kept general.
function toUrl(file) {
	const segments = relative(contentDir, file)
		.replaceAll("\\", "/")
		.replace(/\.mdx$/, "")
		.split("/")
	if (segments.at(-1) === "index") segments.pop()
	return segments.length ? `${baseUrl}/${segments.join("/")}` : baseUrl
}

const files = await walk(contentDir)
const paths = [...new Set(files.map(toUrl))].sort()

const output = {
	_generated: "Generated by scripts/generate-docs-sitemap.mjs. Do not edit by hand.",
	paths,
}

await writeFile(outFile, `${JSON.stringify(output, null, "\t")}\n`)
console.log(`[docs-sitemap] wrote ${paths.length} urls to ${relative(root, outFile)}`)
